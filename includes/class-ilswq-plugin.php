			'ilswq-admin',
			ILSWQ_URL . 'assets/admin.js',
			array( 'jquery' ),
			ILSWQ_VERSION,
			true
		);

		wp_localize_script(
			'ilswq-admin',
			'ILSWQ_Admin',
			array(
				'ajaxUrl'  => admin_url( 'admin-ajax.php' ),
