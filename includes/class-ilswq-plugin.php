	/**
	 * Save settings via AJAX.
	 *
	 * @return void
	 */
	public function ajax_save_settings() {
		$this->verify_ajax();

		$settings = ILSWQ_Settings::from_request( isset( $_POST['settings'] ) ? $_POST['settings'] : array() );
		$settings = ILSWQ_Settings::save( $settings );

		wp_send_json_success(
			array(
				'settings' => $settings,
			)
		);
	}

	/**
	 * Scan page via AJAX.
	 *
	 * @return void
	 */
	public function ajax_scan() {
		$this->verify_ajax();

		$page     = isset( $_POST['page'] ) ? absint( $_POST['page'] ) : 1;
		$settings = ILSWQ_Settings::from_request( isset( $_POST['settings'] ) ? $_POST['settings'] : array() );

		wp_send_json_success( $this->scanner->scan_page( $page, ILSWQ_Scanner::PER_PAGE, $settings ) );
	}

	/**
	 * Convert selected IDs via AJAX.
	 *
	 * @return void
	 */
	public function ajax_convert() {
		$this->verify_ajax();

		$raw_ids = isset( $_POST['ids'] ) && is_array( $_POST['ids'] ) ? wp_unslash( $_POST['ids'] ) : array();
		$ids     = array();
		foreach ( $raw_ids as $raw_id ) {
			if ( is_scalar( $raw_id ) ) {
				$ids[] = absint( $raw_id );
			}
		}
		$ids = array_filter( $ids );

		$settings = ILSWQ_Settings::from_request( isset( $_POST['settings'] ) ? $_POST['settings'] : array() );
		$ids      = array_slice( $ids, 0, (int) $settings['batch_size'] );

		wp_send_json_success(
			array(
				'rows' => $this->converter->convert_batch( $ids, $settings ),
			)
		);
	}

	/**
	 * Cleanup generated WebP files via AJAX.
	 *
	 * @return void
	 */
	public function ajax_cleanup() {
		$this->verify_ajax();

		$reset = isset( $_POST['reset'] ) && is_scalar( $_POST['reset'] ) ? absint( wp_unslash( $_POST['reset'] ) ) : 0;
		if ( $reset > 0 ) {
			delete_option( ILSWQ_OPTION_CLEANUP_PAGE );
		}

		wp_send_json_success( $this->converter->cleanup_generated( 10 ) );
	}

	/**
	 * Validate one generated WebP attachment map.
	 *
	 * @return void
	 */
	public function ajax_validate_webp() {
		$this->verify_ajax();

		$attachment_id = isset( $_POST['id'] ) && is_scalar( $_POST['id'] ) ? absint( wp_unslash( $_POST['id'] ) ) : 0;
		if ( $attachment_id <= 0 ) {
			wp_send_json_error(
				array(
					'message' => __( 'No attachment selected for WebP validation.', 'indexlane-safe-webp-queue' ),
				),
				400
			);
		}

		$map = ILSWQ_Scanner::get_webp_map( $attachment_id );
		if ( empty( $map ) ) {
			wp_send_json_error(
				array(
					'message' => __( 'No generated WebP files were found for this attachment.', 'indexlane-safe-webp-queue' ),
				),
				404
			);
		}

		foreach ( $map as $entry ) {
			if ( is_array( $entry ) && ! empty( $entry['webp'] ) && file_exists( $entry['webp'] ) && ILSWQ_Scanner::is_valid_webp_file( $entry['webp'] ) ) {
				wp_send_json_success(
					array(
					'message' => __( 'Generated WebP file exists and is valid.', 'indexlane-safe-webp-queue' ),
					)
				);
			}
		}

		wp_send_json_error(
			array(
				'message' => __( 'Generated WebP files are missing or invalid.', 'indexlane-safe-webp-queue' ),
			),
			500
		);
	}

	/**
	 * Optionally convert new uploads after metadata has been generated.
	 *
	 * @param array<string,mixed> $metadata Metadata.
	 * @param int                 $attachment_id Attachment ID.
	 * @return array<string,mixed>
	 */
	public function maybe_convert_upload_metadata( $metadata, $attachment_id ) {
		$settings = ILSWQ_Settings::get();
		if ( empty( $settings['auto_uploads'] ) || empty( $metadata ) || ! is_array( $metadata ) ) {
			return $metadata;
		}

		try {
			$row = $this->converter->convert_attachment_with_metadata( (int) $attachment_id, $settings, $metadata );
			if ( ! empty( $row['status'] ) && in_array( $row['status'], array( 'Failed', 'Needs review' ), true ) ) {
				update_post_meta( (int) $attachment_id, ILSWQ_META_LAST_ERROR, isset( $row['reason'] ) ? (string) $row['reason'] : (string) $row['status'] );
			} else {
				delete_post_meta( (int) $attachment_id, ILSWQ_META_LAST_ERROR );
			}
		} catch ( Throwable $exception ) {
			update_post_meta( (int) $attachment_id, ILSWQ_META_LAST_ERROR, $exception->getMessage() );
		}

		return $metadata;
	}

	/**
	 * Verify AJAX request.
	 *
	 * @return void
	 */
	private function verify_ajax() {
		if ( ! current_user_can( 'manage_options' ) ) {
			wp_send_json_error(
				array(
					'message' => __( 'Permission denied.', 'indexlane-safe-webp-queue' ),
				),
				403
			);
		}

		check_ajax_referer( 'ilswq_admin', 'nonce' );
	}
